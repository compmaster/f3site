`ID` INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
`th` INT NOT NULL,
`topic` VARCHAR(50) NOT NULL,
`usr` INT NOT NULL,
`owner` INT NOT NULL,
`st` TINYINT NOT NULL,
`del` INT NOT NULL DEFAULT 0,
