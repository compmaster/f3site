CREATE TABLE IF NOT EXISTS `f3_rss` (
`ID` INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
`auto` TINYINT NOT NULL DEFAULT 1,
`name` VARCHAR(50) NOT NULL DEFAULT '',
