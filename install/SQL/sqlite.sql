CREATE TABLE IF NOT EXISTS `f3_sessions` (
`ID` INTEGER PRIMARY KEY AUTOINCREMENT,
`UID` INT NOT NULL REFERENCES `f3_users`(`ID`),
`key` BINARY(60) NOT NULL,
`date` INT,
`last` INT,
`IP` VARBINARY(16),
`proxy` VARBINARY(16));

CREATE TABLE IF NOT EXISTS `f3_tags` (
`tag` VARCHAR(50) NOT NULL DEFAULT '',
`TYPE` TINYINT NOT NULL DEFAULT 5,
`ID` INT NOT NULL DEFAULT 0,
`num` INT NOT NULL DEFAULT 0);

CREATE INDEX IF NOT EXISTS tag ON f3_tags (tag,TYPE,ID);

